
//Calculate arithmetic functions if decimal values... use float or double "%d, %i"

#include <stdio.h>

int main()
{
    int a;
    float b;
    a = 36;
    b = 6.7;
    printf("a + b = %f\n", a+b);
    printf("a - b = %f\n", a-b);
    printf("a * b = %f\n", a*b);
    printf("a / b = %f\n", a/b);

    return 0;

}