
//Print variables and characters (id - %d, weight - %f since decimal, char - %c)

#include <stdio.h>

int main()
{
    
    int id = 50 ;
    float weight = 48.4 ;
    char section = 'D' ;        
    //use single quotation mark [ ' ] for "char"
    
    printf("ID: %d, Weight: %f, Section: %c\t", id, weight, section) ;

    return 0;
    
}